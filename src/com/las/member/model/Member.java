package com.las.member.model;

public class Member {
	private int customer_id;
	private String customer_pw;
	private int customer_type;
	private String customer_name;
	private String customer_nickname;
	private String customer_gender;
	private String customer_birth_date;
	private String customer_tel;
	private String customer_email;
	private String customer_pic;
	
	public int getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(int customer_id) {
		this.customer_id = customer_id;
	}
	public String getCustomer_pw() {
		return customer_pw;
	}
	public void setCustomer_pw(String customer_pw) {
		this.customer_pw = customer_pw;
	}
	public int getCustomer_type() {
		return customer_type;
	}
	public void setCustomer_type(int customer_type) {
		this.customer_type = customer_type;
	}
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	public String getCustomer_nickname() {
		return customer_nickname;
	}
	public void setCustomer_nickname(String customer_nickname) {
		this.customer_nickname = customer_nickname;
	}
	public String getCustomer_gender() {
		return customer_gender;
	}
	public void setCustomer_gender(String customer_gender) {
		this.customer_gender = customer_gender;
	}
	public String getCustomer_birth_date() {
		return customer_birth_date;
	}
	public void setCustomer_birth_date(String customer_birth_date) {
		this.customer_birth_date = customer_birth_date;
	}
	public String getCustomer_tel() {
		return customer_tel;
	}
	public void setCustomer_tel(String customer_tel) {
		this.customer_tel = customer_tel;
	}
	public String getCustomer_email() {
		return customer_email;
	}
	public void setCustomer_email(String customer_email) {
		this.customer_email = customer_email;
	}
	public String getCustomer_pic() {
		return customer_pic;
	}
	public void setCustomer_pic(String customer_pic) {
		this.customer_pic = customer_pic;
	}

}
